void gimms_string(char *s, int x, int y, int spacing, int scaleMUL, int scaleDIV) {
  while (*s != '\0') {
    draw_gimms(*s, x, y, scaleMUL, scaleDIV);
    x += (spacing*scaleMUL)/scaleDIV;
    s += 1;
  }
}

